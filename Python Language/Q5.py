# Program to get two numbers and calculate sum and product

# Getting input from the user and converting to float (to handle decimals)
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Calculating Sum
sum_result = num1 + num2

# Calculating Product
product_result = num1 * num2

# Printing results
print(f"Sum: {sum_result}")
print(f"Product: {product_result}")