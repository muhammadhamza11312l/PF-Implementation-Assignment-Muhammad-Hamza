# Program to calculate the average of two integers

# Declaring and initializing variables
# Python infers types automatically, but these are integers
num1 = 15
num2 = 25

# Calculating Average
# Note: In Python, division (/) always returns a float
average = (num1 + num2) / 2

# Printing the result
print(f"The average of {num1} and {num2} is: {average}")