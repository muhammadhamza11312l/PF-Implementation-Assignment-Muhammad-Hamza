# Program to calculate distance (s)

# Assigning values to vi (initial velocity) and t (time)
vi = 10  # initial velocity
t = 5    # time
a = 9.8  # acceleration (assuming gravity for this example)

# Calculating s (distance)
# Formula is s = vi * t + 0.5 * a * t * t
s = vi * t + 0.5 * a * t * t

print(f"The calculated distance (s) is: {s}")