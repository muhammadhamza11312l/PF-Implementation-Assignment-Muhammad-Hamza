# Program to print specific output using escape sequences

# 1. C:\Windows> (We need double backslash \\ to print one backslash)
print("C:\\Windows>")

# 2. 'P'    'A'    'K' (Using \t for tabs and \' for single quotes)
print("\'P\'\t\'A\'\t\'K\'")

# 3. "Pakistan" (Using \" to print double quotes)
print("\"Pakistan\"")