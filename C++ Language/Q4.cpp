#include <iostream>
using namespace std;

int main() {
    int years;
    int months;

    // Assign numeric value to year
    years = 5;

    // Calculate months (12 months in a year)
    months = years * 12;

    cout << "Number of years: " << years << endl;
    cout << "Number of months: " << months << endl;

    return 0;
}