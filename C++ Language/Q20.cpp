#include <iostream>
using namespace std;

int main() {
    // Double backslash \\ is needed to print a single \
    cout << "C:\\Windows>" << endl;
    
    // \t adds tab spacing
    cout << "\'P\'\t\'A\'\t\'K\'" << endl;
    
    // \" is needed to print actual double quotes
    cout << "\"Pakistan\"" << endl;

    return 0;
}