#include <stdio.h>

int main() {
    // Declaring and assigning values immediately
    int numOne = 10;
    int numTwo = 20;
    int numThree = 30;

    // Printing the values using format specifiers (%d for integers)
    printf("The first value is: %d\n", numOne);
    printf("The second value is: %d\n", numTwo);
    printf("The third value is: %d\n", numThree);

    return 0;
}